public class CalculadoraSimple{
	static int num1 = 95;
	static int num2 = 30;
	static int resultado;
	
	public static void main(String[] args){
		resultado= num1 + num2;
		System.out.println(resultado);
		resultado= num1 - num2;
		System.out.println(resultado);
		resultado= num1 * num2;
		System.out.println(resultado);
		resultado= num1 / num2;
		System.out.println(resultado);
	}
}